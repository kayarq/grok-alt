def logo_renderable() -> Text:
    """24×24 pixels → 12 terminal rows via half-blocks (▀)."""
    out = Text()
    h, w = len(GRID), len(GRID[0])
    for y in range(0, h, 2):
        if y:
            out.append("\n")
        for x in range(w):
            top = GRID[y][x]
            bot = GRID[y + 1][x] if y + 1 < h else 0
            tc, bc = COLORS[top], COLORS[bot]
            if top == 0 and bot == 0:
                out.append(" ")
            elif top == bot:
                out.append("█", style=tc)
            else:
                out.append("▀", style=f"{tc} on {bc}")
    return out
